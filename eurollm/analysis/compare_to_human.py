# ── Procrustes + RSA alignment ────────────────────────────────────────────────

def _procrustes_align(human_pts, llm_pts):
    """Procrustes alignment of LLM points to human points.

    Both inputs: (n_langs, k) arrays with rows in the same language order.
    Returns (human_standardized, llm_aligned, disparity).
    Disparity is sum of squared differences after optimal alignment (lower=better).
    """
    from scipy.spatial import procrustes
    h_aligned, l_aligned, disparity = procrustes(human_pts, llm_pts)
    return h_aligned, l_aligned, disparity


def _rsa_mantel(human_pts, llm_pts, n_perms=10000):
    """Representational Similarity Analysis via Mantel test.

    Computes pairwise distance matrices for human and LLM points,
    then correlates the upper triangles (Mantel test with permutations).
    Returns (r, p_value).
    """
    from scipy.spatial.distance import pdist, squareform
    from scipy.stats import pearsonr

    d_human = pdist(human_pts, metric="euclidean")
    d_llm = pdist(llm_pts, metric="euclidean")

    r_obs, _ = pearsonr(d_human, d_llm)

    # Permutation test
    n = human_pts.shape[0]
    count_ge = 0
    rng = np.random.RandomState(42)
    for _ in range(n_perms):
        perm = rng.permutation(n)
        d_llm_perm = pdist(llm_pts[perm], metric="euclidean")
        r_perm, _ = pearsonr(d_human, d_llm_perm)
        if r_perm >= r_obs:
            count_ge += 1
    p_value = (count_ge + 1) / (n_perms + 1)

    return r_obs, p_value


def _run_procrustes_rsa_for_config(
    X_human, X_llm, human_labels, llm_labels_all, n_pca=5, n_perms=5000,
):
    """Run Procrustes + RSA for all model families on given data matrices.

    Returns dict: {model_type: {disparity, rsa_r, rsa_p, n_langs, ...}}
    """
    from sklearn.decomposition import PCA

    n_max = min(X_human.shape)
    k = min(n_pca, n_max)
    pca = PCA(n_components=n_max, random_state=42)
    human_pca = pca.fit_transform(X_human.values)[:, :k]
    llm_pca = pca.transform(X_llm.values)[:, :k]

    mt_groups = {}
    for i, label in enumerate(llm_labels_all):
        mt = label.rsplit("_", 1)[0]
        lang = label.rsplit("_", 1)[1]
        mt_groups.setdefault(mt, []).append((i, lang))

    human_lang_set = set(human_labels)
    results = {}

    for mt, members in sorted(mt_groups.items()):
        shared_langs, llm_indices, human_indices = [], [], []
        for llm_idx, lang in members:
            if lang in human_lang_set:
                shared_langs.append(lang)
                llm_indices.append(llm_idx)
                human_indices.append(human_labels.index(lang))

        if len(shared_langs) < 5:
            continue

        h_pts = human_pca[human_indices]
        l_pts = llm_pca[llm_indices]

        _, _, disparity = _procrustes_align(h_pts, l_pts)
        rsa_r, rsa_p = _rsa_mantel(h_pts, l_pts, n_perms=n_perms)

        results[mt] = {
            "disparity": disparity,
            "rsa_r": rsa_r,
            "rsa_p": rsa_p,
            "n_langs": len(shared_langs),
            "n_qs": X_human.shape[1],
        }

    return results


def plot_procrustes_rsa(
    llm_dists, human_dists, questions, jsd_df, figures_dir: Path,
):
    """Procrustes + RSA sweep across multiple filter strategies and thresholds.

    Filter types:
    - JSD: exclude questions with mean JSD above threshold
    - Bias: exclude questions with mean position bias above threshold
    - P_valid: exclude questions with mean P_valid below threshold
    - Combined: require P_valid > thresh AND bias < thresh

    Generates:
    - Line plot: RSA r vs number of questions retained, per filter type
    - Line plot: Procrustes disparity vs number of questions retained
    - Best-config Procrustes scatter
    """
    from sklearn.decomposition import PCA

    _, _, all_qs = _prepare_ev_matrices(llm_dists, human_dists, questions)
    all_qs_set = set(all_qs)
    filtered_jsd = jsd_df[~jsd_df["model_type"].isin(MODELS_EXCLUDE)]

    # Compute per-question quality stats (methodology quality, not cultural distance)
    agg_dict = dict(
        mean_jsd=("jsd", "mean"),
        mean_bias=("position_bias_magnitude", "mean"),
    )
    if "p_valid_mean" in filtered_jsd.columns:
        agg_dict["mean_pvalid"] = ("p_valid_mean", "mean")
    q_stats = filtered_jsd.groupby("question_id").agg(**agg_dict).reset_index()
    if "mean_pvalid" not in q_stats.columns:
        q_stats["mean_pvalid"] = 1.0

    print(f"  Per-question quality: P_valid mean={q_stats['mean_pvalid'].mean():.3f}, "
          f"Bias mean={q_stats['mean_bias'].mean():.3f}")

    # Build filter configs: each is (label, filter_type, threshold, excluded_qids, n_kept)
    # Methodology-quality filters (preferred): Bias and P_valid measure whether the
    # LLM produced reliable answers, without penalizing genuine cultural disagreement.
    # JSD filters (for comparison): also remove questions where LLM disagrees with humans,
    # which conflates methodology failures with real cultural signal.
    filter_configs = []

    # P_valid thresholds (exclude low-quality questions)
    for thresh in np.arange(0.75, 0.95, 0.01):
        excl = set(q_stats[q_stats["mean_pvalid"] < thresh]["question_id"])
        n_kept = len(all_qs_set - excl)
        if n_kept >= 10:
            filter_configs.append((f"Pv>{thresh:.2f}", "P_valid", thresh, excl, n_kept))

    # Bias thresholds (exclude high-bias questions)
    for thresh in np.arange(0.20, 0.65, 0.025):
        excl = set(q_stats[q_stats["mean_bias"] > thresh]["question_id"])
        n_kept = len(all_qs_set - excl)
        if n_kept >= 10:
            filter_configs.append((f"Bias<{thresh:.3f}", "Bias", thresh, excl, n_kept))

    # Quality-only combined: P_valid > thresh AND Bias < thresh (no JSD!)
    for pv_t in np.arange(0.78, 0.92, 0.02):
        for bias_t in np.arange(0.30, 0.55, 0.05):
            excl_pv = set(q_stats[q_stats["mean_pvalid"] < pv_t]["question_id"])
            excl_bias = set(q_stats[q_stats["mean_bias"] > bias_t]["question_id"])
            excl = excl_pv | excl_bias
            n_kept = len(all_qs_set - excl)
            if n_kept >= 10:
                filter_configs.append((
                    f"Pv>{pv_t:.2f}+Bias<{bias_t:.2f}", "Quality",
                    (pv_t, bias_t), excl, n_kept,
                ))

    # JSD thresholds (for comparison — note this conflates quality with cultural distance)
    for thresh in np.arange(0.15, 0.55, 0.025):
        excl = set(q_stats[q_stats["mean_jsd"] > thresh]["question_id"])
        n_kept = len(all_qs_set - excl)
        if n_kept >= 10:
            filter_configs.append((f"JSD<{thresh:.3f}", "JSD", thresh, excl, n_kept))

    # No filter baseline
    filter_configs.append(("No filter", "None", None, set(), len(all_qs_set)))

    print(f"  Running {len(filter_configs)} filter configurations...")

    # Run Procrustes+RSA for each config
    sweep_rows = []
    best_rsa = {"r": -1, "config": None, "results": None}

    for label, ftype, thresh, excl, n_kept in filter_configs:
        X_human, X_llm, shared_qs = _prepare_ev_matrices(
            llm_dists, human_dists, questions, exclude_qs=excl if excl else None
        )
        if X_human.shape[1] < 10:
            continue

        results = _run_procrustes_rsa_for_config(
            X_human, X_llm,
            list(X_human.index), list(X_llm.index),
            n_pca=5, n_perms=5000,
        )

        for mt, res in results.items():
            sweep_rows.append({
                "filter_label": label,
                "filter_type": ftype,
                "threshold": str(thresh),
                "n_questions": n_kept,
                "model_type": mt,
                **res,
            })

        # Track best RSA across all models (average)
        if results:
            avg_rsa = np.mean([r["rsa_r"] for r in results.values()])
            if avg_rsa > best_rsa["r"]:
                best_rsa = {"r": avg_rsa, "config": label, "results": results,
                            "excl": excl, "n_kept": n_kept}

    sweep_df = pd.DataFrame(sweep_rows)
    if sweep_df.empty:
        print("  No valid filter configurations produced results")
        return

    # --- Figure 1: RSA r vs n_questions, colored by filter type ---
    fig, axes = plt.subplots(1, 2, figsize=(16, 6))

    type_colors = {
        "Quality": "#2ca02c", "P_valid": "#9467bd", "Bias": "#1f77b4",
        "JSD": "#d62728", "None": "#999999",
    }
    type_markers = {
        "Quality": "D", "P_valid": "^", "Bias": "s",
        "JSD": "o", "None": "*",
    }

    for metric, ax, ylabel, title, better in [
        ("rsa_r", axes[0], "RSA Mantel r", "RSA: Cultural Distance Correlation", "higher"),
        ("disparity", axes[1], "Procrustes Disparity", "Procrustes: Alignment Residual", "lower"),
    ]:
        for mt in sorted(sweep_df["model_type"].unique()):
            mt_df = sweep_df[sweep_df["model_type"] == mt]
            for ftype in sorted(mt_df["filter_type"].unique()):
                sub = mt_df[mt_df["filter_type"] == ftype].sort_values("n_questions")
                color = type_colors.get(ftype, "#999999")
                marker = type_markers.get(ftype, "o")
                mt_label = MODEL_LABELS.get(mt, mt)
                alpha = 0.7
                # Only label once per filter type (first model)
                label = f"{ftype}" if mt == sorted(sweep_df["model_type"].unique())[0] else None
                ax.scatter(sub["n_questions"], sub[metric],
                           c=color, marker=marker, s=20, alpha=alpha)
                if label:
                    ax.plot([], [], color=color, marker=marker, linestyle="none",
                            markersize=6, label=label)

        # Plot per-model trend lines for quality-only filters
        for ftype_line in ["Quality", "P_valid", "Bias"]:
            for mt in sorted(sweep_df["model_type"].unique()):
                sub = sweep_df[(sweep_df["model_type"] == mt) & (sweep_df["filter_type"] == ftype_line)]
                sub = sub.sort_values("n_questions")
                if len(sub) > 1:
                    mt_label = MODEL_LABELS.get(mt, mt)
                    ax.plot(sub["n_questions"], sub[metric],
                            color=MODEL_COLORS.get(mt, "#999999"), linewidth=1,
                            alpha=0.5, linestyle="--" if ftype_line == "JSD" else "-")

        ax.set_xlabel("Number of Questions Retained")
        ax.set_ylabel(ylabel)
        ax.set_title(title)
        ax.legend(fontsize=7, loc="best")
        ax.grid(True, alpha=0.3)

    plt.suptitle("Filter Sweep: Procrustes + RSA vs Question Selection (PCA-5)",
                 fontsize=13, fontweight="bold")
