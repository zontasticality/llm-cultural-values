def run_figure(
    figure: str,
    jsd_df: pd.DataFrame,
    llm_dists: dict,
    human_dists: dict,
    questions: dict,
    figures_dir: Path,
):
    """Dispatch to individual figure generators.

    Figures used in the analysis report (--figure report):
        F2_inglehart_welzel.png   — Inglehart-Welzel cultural map
        F3_ev_scatter.png         — EV scatter with z-scored row
        F4_jsd_heatmap.png        — Bias-weighted JSD heatmap
        F4_jsd_distribution.png   — Per-question JSD histogram
        F5_deepdive.png           — Per-question deep dive (3×3 grid)
        F5_umap_grid.png          — 4×2 UMAP grid (metric × filter + uncentered)
        jsd_vs_bias.png           — JSD vs position bias diagnostic
        umap_sensitivity.png      — UMAP hyperparameter sensitivity sweep
      (F6_pvalid_heatmap.png and F7_position_bias.png are generated by analyze.py)
    """
    # Report figures: exactly what the typst report references
    report_figs = ("report", "all")

    if figure in (*report_figs, "umap_grid"):
        print("\n  Generating UMAP Grid (4×2: metric × filter)...")
        try:
            plot_umap_grid(llm_dists, human_dists, questions, jsd_df, figures_dir)
        except ImportError:
            print("  Skipping UMAP grid: umap-learn not installed")

    if figure in (*report_figs, "jsd_dist"):
        print("\n  Generating JSD Distribution...")
        plot_jsd_distribution(jsd_df, figures_dir)

    if figure in (*report_figs, "iw"):
        print("\n  Generating Inglehart-Welzel Composite...")
        iw_df = compute_iw_composites(llm_dists, human_dists, questions)
        if not iw_df.empty:
            plot_iw_composite(iw_df, figures_dir)
        else:
            print("  Skipping IW: insufficient question data")

    if figure in (*report_figs, "scatter"):
        print("\n  Generating EV Scatter + Z-Score...")
        plot_scatter_ev(jsd_df, figures_dir)

    if figure in (*report_figs, "heatmap"):
        print("\n  Generating JSD Heatmap...")
        plot_jsd_heatmap(jsd_df, figures_dir)

    if figure in (*report_figs, "deepdive"):
        print("\n  Generating Deep Dive...")
        plot_deepdive(jsd_df, llm_dists, human_dists, figures_dir)

    if figure in (*report_figs, "jsd_vs_bias"):
        print("\n  Generating JSD vs Bias scatter...")
        plot_jsd_vs_bias(jsd_df, figures_dir)

    if figure in (*report_figs, "umap_sensitivity"):
        print("\n  Generating UMAP Sensitivity sweep...")
        try:
            plot_umap_sensitivity(llm_dists, human_dists, questions, jsd_df, figures_dir)
        except ImportError:
            print("  Skipping UMAP sensitivity: umap-learn not installed")

    if figure in (*report_figs, "pca"):
        print("\n  Generating PCA cultural geography...")
        plot_pca_comparison(llm_dists, human_dists, questions, jsd_df, figures_dir)

    # Supplementary figures only when running "all" or explicitly
    if figure in ("all", "procrustes"):
        print("\n  Generating Procrustes + RSA alignment...")
        plot_procrustes_rsa(llm_dists, human_dists, questions, jsd_df, figures_dir)

    if figure == "all":
        print("\n  Generating supplementary figures...")
        plot_jsd_by_qtype(jsd_df, figures_dir)
        plot_model_comparison(jsd_df, figures_dir)


def main():
    PROJECT_ROOT = Path(__file__).resolve().parent.parent

    parser = argparse.ArgumentParser(
        description="Compare LLM cultural value distributions to human survey data"
    )
    parser.add_argument("--results-dir", type=Path,
                        default=PROJECT_ROOT / "results")
    parser.add_argument("--human", type=Path,
                        default=PROJECT_ROOT / "human_data" / "data" / "human_distributions.parquet")
    parser.add_argument("--questions", type=Path,
                        default=PROJECT_ROOT / "data" / "questions.json")
    parser.add_argument("--figures-dir", type=Path,
                        default=PROJECT_ROOT / "figures")
    parser.add_argument("--db", type=str, default=None,
                        help="Path to survey.db (loads LLM + human data from DB)")
    parser.add_argument("--figure", type=str, default="all",
                        choices=["all", "report", "umap_grid", "umap_sensitivity", "pca", "procrustes", "iw", "scatter", "heatmap", "jsd_dist", "deepdive", "jsd_vs_bias"],
                        help="Which figure to generate (default: all). "
                             "'report' generates only the 6 figures used in analysis_report.typ "
                             "and compiles the PDF.")
    args = parser.parse_args()

    args.figures_dir.mkdir(parents=True, exist_ok=True)

    # Load data
    print("Loading data...")
    llm_df = load_llm_results(args.results_dir, db_path=args.db)
    if args.db:
        from db.load import load_human_distributions
        human_df = load_human_distributions(args.db)
    else:
        human_df = pd.read_parquet(args.human)
    questions = load_question_metadata(args.questions)
    print(f"Human data: {len(human_df)} rows, "
          f"{human_df['lang'].nunique()} languages, "
          f"{human_df['question_id'].nunique()} questions")

    # Build distribution lookups
    print("\nBuilding distributions...")
    llm_dists = build_distributions(
        llm_df, "prob_averaged", ["model_type", "lang", "question_id"]
    )
    human_dists = build_distributions(
        human_df, "prob_human", ["lang", "question_id"]
    )
    print(f"  LLM distributions: {len(llm_dists)}")
    print(f"  Human distributions: {len(human_dists)}")

    # Compute JSD for all (model_type, lang, question_id) triples
    print("\nComputing JSD...")
    jsd_df = compute_jsd_per_question(llm_dists, human_dists, questions)
    print(f"  Computed {len(jsd_df)} JSD values")

    if jsd_df.empty:
        print("ERROR: No overlapping (model_type, lang, question_id) between LLM and human data")
        sys.exit(1)

    # Join position bias and P_valid data for quality-weighted metrics
    if "position_bias_magnitude" in llm_df.columns:
        llm_df["_p_valid_avg"] = (llm_df["p_valid_forward"] + llm_df["p_valid_reversed"]) / 2
        quality_df = llm_df.groupby(
            ["model_type", "lang", "question_id"]
        ).agg(
            position_bias_magnitude=("position_bias_magnitude", "first"),
            p_valid_mean=("_p_valid_avg", "mean"),
        ).reset_index()
        jsd_df = jsd_df.merge(
            quality_df, on=["model_type", "lang", "question_id"], how="left"
        )
        jsd_df["bias_weight"] = 1.0 - jsd_df["position_bias_magnitude"].fillna(0).clip(0, 1)
        jsd_df["p_valid_mean"] = jsd_df["p_valid_mean"].fillna(0.0)
        print(f"  Added bias weights (mean weight: {jsd_df['bias_weight'].mean():.3f})")
        print(f"  Added P_valid (mean: {jsd_df['p_valid_mean'].mean():.3f})")
    else:
        jsd_df["position_bias_magnitude"] = 0.0
        jsd_df["bias_weight"] = 1.0
        jsd_df["p_valid_mean"] = 1.0

    # Print summary
    print_summary(jsd_df)

    # Generate figures
    fig_dir = args.figures_dir
    if args.figure == "report":
        fig_dir = args.figures_dir / "main"
        fig_dir.mkdir(parents=True, exist_ok=True)
    print(f"\nGenerating figures in {fig_dir}/...")
    run_figure(args.figure, jsd_df, llm_dists, human_dists, questions, fig_dir)

    # Save JSD data for further analysis
    out = args.figures_dir.parent / "human_comparison.parquet"
    jsd_df.to_parquet(out, index=False)
    print(f"\nSaved comparison data to {out}")

    # Compile typst report when generating report figures
    if args.figure == "report":
        import shutil
        import subprocess
        report_typ = args.figures_dir / "analysis_report.typ"
        if report_typ.exists() and shutil.which("typst"):
            print(f"\nCompiling {report_typ}...")
            result = subprocess.run(
                ["typst", "compile", str(report_typ)],
                capture_output=True, text=True,
            )
            if result.returncode == 0:
                print(f"  Saved {report_typ.with_suffix('.pdf')}")
            else:
                print(f"  typst compile failed: {result.stderr.strip()}")
        elif not report_typ.exists():
            print(f"\n  Skipping typst compile: {report_typ} not found")
        else:
            print("\n  Skipping typst compile: typst not installed")

    print("\nDone!")


if __name__ == "__main__":
    main()
