def _apply_human_fitted_centering(X_human, X_llm):
    """Apply human-fitted centering: shift LLM families to human centroid.

    Returns centered copies. Human stays at natural coords.
    """
    human_mean = X_human.mean(axis=0)
    X_human_c = X_human.copy()
    X_llm_c = X_llm.copy()

    llm_labels = list(X_llm.index)
    mt_groups = {}
    for label in llm_labels:
        mt = label.rsplit("_", 1)[0]
        mt_groups.setdefault(mt, []).append(label)

    for mt, labels in mt_groups.items():
        group = X_llm.loc[labels]
        X_llm_c.loc[labels] = (group - group.mean(axis=0) + human_mean).values

    return X_human_c, X_llm_c


def _variance_match_pca(human_pca, llm_pca, n_components=None):
    """Variance-match LLM PCA projections to human statistics per axis.

    For each PC axis j, transforms LLM values so their mean and std
    match the human mean and std:
        x'[j] = (x[j] - mean_llm[j]) * (std_human[j] / std_llm[j]) + mean_human[j]

    This removes both the shift (centering) and the scale compression
    (attenuation), leaving only the relative structure for comparison.
    """
    k = n_components or human_pca.shape[1]
    h = human_pca[:, :k].copy()
    l = llm_pca[:, :k].copy()

    h_mean = h.mean(axis=0)
    h_std = h.std(axis=0)

    # Per model family: group by model type prefix
    # But we only have the raw arrays here, not labels — so do it globally
    # (caller can pass per-family slices if needed)
    l_mean = l.mean(axis=0)
    l_std = l.std(axis=0)

    # Avoid division by zero on axes with no LLM variance
    l_std = np.where(l_std > 1e-10, l_std, 1.0)

    l_matched = (l - l_mean) * (h_std / l_std) + h_mean
    return h, l_matched


def _variance_match_pca_per_family(human_pca, llm_pca, llm_labels, n_components=None):
    """Variance-match per model family in PCA space.

    Each model family is independently rescaled to match human statistics.
    """
    k = n_components or human_pca.shape[1]
    h = human_pca[:, :k].copy()
    l = llm_pca[:, :k].copy()

    h_mean = h.mean(axis=0)
    h_std = h.std(axis=0)

    mt_groups = {}
    for i, label in enumerate(llm_labels):
        mt = label.rsplit("_", 1)[0]
        mt_groups.setdefault(mt, []).append(i)

    l_matched = l.copy()
    for mt, indices in mt_groups.items():
        family = l[indices]
        f_mean = family.mean(axis=0)
        f_std = family.std(axis=0)
        f_std = np.where(f_std > 1e-10, f_std, 1.0)
        l_matched[indices] = (family - f_mean) * (h_std / f_std) + h_mean

    return h, l_matched


